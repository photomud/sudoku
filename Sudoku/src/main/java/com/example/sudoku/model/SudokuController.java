    public static Board createEasyGame(){
        int[][] fixedItems = {
                new int[] {5,0,0,0,0,0,6,0,1},//row1
                new int[] {6,0,0,0,0,2,4,0,0},
                new int[] {0,0,0,7,0,1,0,0,2},
                new int[] {0,0,4,0,2,0,0,3,0},//row4
                new int[] {0,3,0,0,0,0,0,9,0},
                new int[] {0,0,0,0,4,0,0,0,0},
                new int[] {0,2,0,1,0,5,9,0,0},//row7
                new int[] {0,0,0,6,0,0,0,0,5},
                new int[] {0,0,6,0,3,0,0,0,7}

        };

        Board easyBoard = createBoardWithFixedElts(fixedItems);

        return easyBoard;
    }

    private static Board createBoardWithFixedElts(int[][] fixedElts){
        Board gameBoard = new Board();
        for (int i = 0; i < fixedElts.length; i++) {
            for (int j = 0; j < fixedElts[i].length; j++) {
              int value = fixedElts[i][j];
              if (value != 0){
                  gameBoard.setFixedTile(i, j, value);
              }
            }
        }
        return gameBoard;
    }



